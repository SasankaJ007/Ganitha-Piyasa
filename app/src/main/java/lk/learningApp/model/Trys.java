package lk.learningApp.model;

public class Trys {

    private int correctTime;
    private int shownTime;
    private String symbol;

    public Trys() {

    }

    public int getCorrectTime() {
        return correctTime;
    }

    public void setCorrectTime(int correctTime) {
        this.correctTime = correctTime;
    }

    public int getShownTime() {
        return shownTime;
    }

    public void setShownTime(int shownTime) {
        this.shownTime = shownTime;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }
}
